    private void jButtonSelecionarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarUsuarioActionPerformed
        // TODO add your handling code here:

        try {

            if( usuario == null ) {
                usuario = new Usuario();
            }

            JFrameConsultaUsuario jFrameConsulta;
            jFrameConsulta = new JFrameConsultaUsuario( true, false, usuario );

            jFrameConsulta.addWindowListener( new java.awt.event.WindowAdapter(){
                @Override
                public void windowClosed(java.awt.event.WindowEvent evt) {
                    if( usuario.getNome() != null ) {
                        jTextFieldUsuario.setText( usuario.getNome() );
                    }
                }
            } );

            jFrameConsulta.setVisible(true);
        } catch( Exception ex ) {
            LogTrack.getInstance().addException(ex, true, this);
            usuario = null;
        }
    }//GEN-LAST:event_jButtonSelecionarUsuarioActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:

        int row = jTableConsulta.getSelectedRow();
        if( row != -1 ) {

            System.out.println("Alterar");

            String data =
            result.getValueAt( row, 2 ).toString();
            
            int idUsuario =
            (int) result.getValueAt( row, 6 );

            NotaFiscal notaFiscal = new NotaFiscal();
            Usuario usuario = new Usuario();

            try {

                usuario.setIdUsuario(idUsuario);
                usuario.load();

                notaFiscal.setDataEmissao(data);
                notaFiscal.setUsuario(usuario);

                notaFiscal.load();

                JFrameNotaFiscalCRUD jFrameCRUD;
                jFrameCRUD = new JFrameNotaFiscalCRUD( notaFiscal, false );

                jFrameCRUD.addWindowListener( new java.awt.event.WindowAdapter(){
                    @Override
                    public void windowClosed(java.awt.event.WindowEvent evt) {
                        System.out.println("Atualizar");
                        try {
                            result.setQuery( query );
                        } catch ( Exception ex ) {
                            ex.printStackTrace();
                        }
                    }
                } );

                jFrameCRUD.setVisible( true );

            } catch( Exception ex ) {
                ex.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButtonAlterarActionPerformed

